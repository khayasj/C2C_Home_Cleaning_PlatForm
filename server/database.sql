CREATE DATABASE IF NOT EXISTS cleaning_app;

USE cleaning_app;

DROP TABLE IF EXISTS serviceshortlist;
DROP TABLE IF EXISTS confirmmatches;
DROP TABLE IF EXISTS cleaningservice;
DROP TABLE IF EXISTS servicecategory;

DROP TABLE IF EXISTS useraccount;
DROP TABLE IF EXISTS userprofile;

CREATE TABLE userprofile (
   profilename VARCHAR(50) PRIMARY KEY,
   profiletype VARCHAR(50) NOT NULL,
   profiledescription VARCHAR(50) 
);

CREATE TABLE useraccount (
	username VARCHAR(50) PRIMARY KEY,
	password VARCHAR(255) NOT NULL,
	firstname VARCHAR(100) NOT NULL,
	lastname VARCHAR(100) NOT NULL,
	email VARCHAR(100) UNIQUE,
	phone VARCHAR(20) NOT NULL,
	profilename VARCHAR(50),
	FOREIGN KEY (profilename) REFERENCES userprofile(profilename) ON DELETE CASCADE
);


CREATE TABLE servicecategory (
    servicecategory VARCHAR(50) PRIMARY KEY,
    servicedescription VARCHAR(100)
);

CREATE TABLE cleaningservice (
    cleanerusername VARCHAR(50),
    cleaningservicename VARCHAR(100),
    cleaningdescription VARCHAR(100),
    servicecategory VARCHAR(50),
    price DECIMAL(10,2),
    numofviews INT,
    numofshortlist INT,
    PRIMARY KEY (cleanerusername, cleaningservicename),
	  FOREIGN KEY (cleanerusername) REFERENCES useraccount(username) ON DELETE CASCADE,
    FOREIGN KEY (servicecategory) REFERENCES servicecategory(servicecategory) ON DELETE CASCADE
);

CREATE TABLE confirmmatches (
    cleanerusername VARCHAR(50),
    homeownerusername VARCHAR(50),
    cleaningservicename VARCHAR(100),
    cleaninglocation VARCHAR(100),
    cleaningdate VARCHAR(50),
    cleaningstarttime TIME,
    cleaningendtime TIME,
    PRIMARY KEY (cleanerusername, cleaningservicename, homeownerusername, cleaningdate, cleaningstarttime),
    FOREIGN KEY (homeownerusername) REFERENCES useraccount(username) ON DELETE CASCADE,
    FOREIGN KEY (cleanerusername, cleaningservicename) REFERENCES cleaningservice(cleanerusername, cleaningservicename) ON DELETE CASCADE
);

CREATE TABLE serviceshortlist (
    homeownerusername VARCHAR(50),
    cleanerusername VARCHAR(50),
    cleaningservicename VARCHAR(100),
    PRIMARY KEY (homeownerusername, cleanerusername, cleaningservicename),
    FOREIGN KEY (homeownerusername) REFERENCES useraccount(username) ON DELETE CASCADE,
    FOREIGN KEY (cleanerusername, cleaningservicename) REFERENCES cleaningservice(cleanerusername, cleaningservicename) ON DELETE CASCADE
);

